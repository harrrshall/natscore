"""Aggregate per-ablation eval JSONs into a single docs/BENCHMARK.md table.

Walks `outputs/<run_name>/eval_dev.json` for every run that has one, builds a
markdown comparison table sorted by pairwise accuracy, and replaces (or
appends) an "## Ablations" section in docs/BENCHMARK.md.

Usage:
    python scripts/05_aggregate_benchmark.py                       # default scan
    python scripts/05_aggregate_benchmark.py --outputs-dir outputs # explicit
"""

from __future__ import annotations

import argparse
import json
import re
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
DEFAULT_OUTPUTS = REPO_ROOT / "outputs"
BENCHMARK_MD = REPO_ROOT / "docs" / "BENCHMARK.md"
ABLATION_SECTION_HEADER = "## Ablations (M5b)"


def _gather(outputs_dir: Path) -> list[dict]:
    rows: list[dict] = []
    for json_path in sorted(outputs_dir.glob("*/eval_dev.json")):
        try:
            with json_path.open() as fh:
                rows.append(json.load(fh))
        except json.JSONDecodeError as exc:
            print(f"[warn] skipping {json_path}: {exc}", file=sys.stderr)
    # Also pick up older 03_evaluate.py output (eval_results.json)
    for json_path in sorted(outputs_dir.glob("*/eval_results.json")):
        try:
            with json_path.open() as fh:
                raw = json.load(fh)
            # Normalize old schema -> new schema
            if "result" in raw:
                r = raw["result"]
                rows.append({
                    "ablation_config": "(headline)",
                    "run_name": raw.get("label", json_path.parent.name),
                    "n_pairs": r.get("n_pairs"),
                    "pairwise_accuracy": r.get("pairwise_accuracy"),
                    "mean_margin": r.get("mean_margin"),
                    "ci_low": r.get("ci_low"),
                    "ci_high": r.get("ci_high"),
                    "ece": raw.get("calibration", {}).get("ece"),
                    "per_subset": r.get("per_subset", {}),
                    "per_language": r.get("per_language", {}),
                })
        except json.JSONDecodeError as exc:
            print(f"[warn] skipping {json_path}: {exc}", file=sys.stderr)
    return rows


def _build_markdown(rows: list[dict]) -> str:
    rows = sorted(rows, key=lambda r: r.get("pairwise_accuracy") or 0, reverse=True)
    out = [
        ABLATION_SECTION_HEADER,
        "",
        "Auto-generated by `scripts/05_aggregate_benchmark.py`. Re-run after each new",
        "Kaggle ablation result lands.",
        "",
        "| Run | n_pairs | Pairwise acc | 95% CI | Mean margin | ECE |",
        "|---|---|---|---|---|---|",
    ]
    for r in rows:
        run = r.get("run_name", "?")
        n = r.get("n_pairs", "?")
        acc = r.get("pairwise_accuracy")
        lo = r.get("ci_low"); hi = r.get("ci_high")
        margin = r.get("mean_margin")
        ece = r.get("ece")
        acc_s = f"{100*acc:.2f}%" if acc is not None else "?"
        ci_s = (f"[{100*lo:.2f}, {100*hi:.2f}]"
                if (lo is not None and hi is not None) else "—")
        m_s = f"{margin:+.3f}" if margin is not None else "—"
        e_s = f"{100*ece:.2f}%" if ece is not None else "—"
        out.append(f"| {run} | {n} | {acc_s} | {ci_s} | {m_s} | {e_s} |")
    return "\n".join(out) + "\n"


def _splice_section(existing: str, new_section: str) -> str:
    """Replace any existing '## Ablations (M5b)' block, else append."""
    pattern = re.compile(
        rf"^{re.escape(ABLATION_SECTION_HEADER)}.*?(?=^## |\Z)",
        re.MULTILINE | re.DOTALL,
    )
    if pattern.search(existing):
        return pattern.sub(new_section.rstrip() + "\n\n", existing)
    return existing.rstrip() + "\n\n" + new_section


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--outputs-dir", type=Path, default=DEFAULT_OUTPUTS)
    parser.add_argument("--write", action="store_true",
                        help="Write into docs/BENCHMARK.md. Without this flag, prints to stdout.")
    args = parser.parse_args()

    rows = _gather(args.outputs_dir)
    if not rows:
        print(f"No eval JSONs found under {args.outputs_dir}/*/eval_*.json", file=sys.stderr)
        sys.exit(1)
    section = _build_markdown(rows)

    if not args.write:
        print(section)
        return

    if not BENCHMARK_MD.exists():
        BENCHMARK_MD.parent.mkdir(parents=True, exist_ok=True)
        BENCHMARK_MD.write_text("# NatScore — Benchmark\n\n" + section)
    else:
        spliced = _splice_section(BENCHMARK_MD.read_text(), section)
        BENCHMARK_MD.write_text(spliced)
    print(f"Wrote {len(rows)} ablation row(s) to {BENCHMARK_MD}")


if __name__ == "__main__":
    main()
